#include <stdio.h>

void division(void)
{
	printf("0 / 3   = %d\n", (0/3));
	printf("1 / 3   = %d\n", (1/3));
	printf("2 / 3   = %d\n", (2/3));
	printf("3 / 3   = %d\n", (3/3));
	printf("4 / 3   = %d\n", (4/3));
	printf("5 / 3   = %d\n", (5/3));
	printf("6 / 3   = %d\n", (6/3));

	printf("0 / 3.0 = %f\n", (0/3.0));
	printf("1 / 3.0 = %f\n", (1/3.0));
	printf("2 / 3.0 = %f\n", (2/3.0));
	printf("3 / 3.0 = %f\n", (3/3.0));
	printf("4 / 3.0 = %f\n", (4/3.0));
	printf("5 / 3.0 = %f\n", (5/3.0));
	printf("6 / 3.0 = %f\n", (6/3.0));

	printf("\n");
}
